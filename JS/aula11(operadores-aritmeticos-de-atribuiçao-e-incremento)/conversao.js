// Conversão de string para number

let num1 = 10;
let num2 = '5';
console.log(num1 + num2)
console.log(typeof num2)

num1 = 10;
num2 = parseInt('5.2'); // conversão para numeros inteiros
console.log(num1 + num2)
console.log(typeof num2)

num1 = 10;
num2 = parseFloat('5.2'); // conversão para numeros decimal
console.log(num1 + num2)
console.log(typeof num2)

num1 = 10;
num2 = Number('5.2');
console.log(num1 + num2)
console.log(typeof num2)


